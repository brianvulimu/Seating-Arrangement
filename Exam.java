    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        String examNo=tf1.getText();
            String courseCode=tf2.getText();
            String courseTitle=tf3.getText();
            String academicYear=cb1.getSelectedItem().toString();
            String semester=cb2.getSelectedItem().toString();
            String examType="Main";
            
            if(rbtn2.isSelected()){
            examType="Special";
            }
            else if(rbtn3.isSelected()){
            examType="Retake";
            }
            if(verifyFields()){
                
                PreparedStatement ps;
                DirStateFactory.Result rs;
                String regQuery="INSERT INTO `exam`(`examNo`, `courseCode`,`courseTitle` , `acadYear`, `semester`,`examType`) VALUES (?,?,?,?,?,?)";
                
                    try {
                        ps=(PreparedStatement) connection.getConnection().prepareStatement(regQuery);
                        ps.setString(1, examNo);
                        ps.setString(2,courseCode);
                        ps.setString(3,courseTitle);
                        ps.setString(4, academicYear);
                        ps.setString(5, semester);
                        ps.setString(6, examType);
                        
                        if(ps.executeUpdate()!=0){
                            JOptionPane.showMessageDialog(null,"Data has been inserted successfully","Add Data",2);
                        }else{
                        JOptionPane.showMessageDialog(null,"Failed to add data","Failed!!",2);
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
                    }
                
                }
            
    }//GEN-LAST:event_addActionPerformed

