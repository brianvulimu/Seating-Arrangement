    public void show_exam(){
    ArrayList<Exams> list=userList();
    DefaultTableModel model=(DefaultTableModel) jTable_Display_Exam.getModel();
    Object[] row=new Object[6];
    for(int i=0;i<list.size();i++){
    row[0]=list.get(i).getExamNo();
    row[1]=list.get(i).getCourseCode();
    row[2]=list.get(i).getCourseTitle();
    row[3]=list.get(i).getAcadYear();
    row[4]=list.get(i).getSemester();
    row[5]=list.get(i).getExamType();
    model.addRow(row);
    }
    }
    public ArrayList<Exams> userList(){
    ArrayList<Exams> studentsList=new ArrayList<>();
    PreparedStatement st;
            ResultSet rs;
                      
            
            String query="SELECT * FROM exam";
            try {
                st=(PreparedStatement) connection.getConnection().prepareStatement(query);
                
                Exams s;
            
            rs= (ResultSet) st.executeQuery();
            
            while(rs.next()){
            
                s=new Exams(rs.getString("examNo"),rs.getString("courseCode"),rs.getString("courseTitle"),rs.getString("acadYear"),rs.getString("semester"),rs.getString("examType"));
                studentsList.add(s);
            }
            
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
            return studentsList;
    }

